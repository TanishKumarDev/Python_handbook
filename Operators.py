# Example of Arithmetic Operators in Python:
num1 = 10
num2 = 5

print("addition", num1 + num2)
print("subtraction", num1 - num2)
print("multiplication", num1 * num2)
print("division", num1 / num2) # return 2.0
print("modulus", num1 % num2) # return remainder
print("exponentiation", num1 ** num2) # return 100000

# Example of Comparison Operators in Python
print("Comparison Operators:",  num1, num2, sep=", ")

print("=", num1 == num2) # return False
print("!=", num1 != num2) # return True
print(">", num1 > num2) # return True
print("<", num1 < num2) # return False
print(">=", num1 >= num2) # return True
print("<=", num1 <= num2) # return False

# Logical Operators in Python
a = True
b = False

print("Logical Operators:",  a, b, sep=", ")

print("and", a and b) # return False
print("or", a or b) # return True
print("not", not a) # return False

